char	*ft_strrev(char *str)
{
	int tmp;
	int b;
	int c;

	c = 0;
	b = 0;
	while (str[c])
		c++;
	c--;
	while (b < c)
	{
		tmp = str[b];
		str[b] = str[c];
		str[c] = tmp;
		c--;
		b++;
	}
	return (str);
}
